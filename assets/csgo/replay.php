      <h2>cs:go demo</h2>
        <p>If you ever wonder how to record all your pro plays &amp; make a frag movie out of it, this guide might
      be useful to you. The Source Engine provides a set of tools to record a game to a demo file for
      possible editing and later playback. These functions are provided via console commands or GUI tools
