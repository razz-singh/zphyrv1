    <h2>Replay Recording &amp; Editing</h2>    
    <div class="container col-md-8 col-md-offset-2">
      <p>Open Dota 2, download your replay:</p>
        <ol>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="0s">Go to the Watch tab, then find the replay you just downloaded under the
    downloads tab, rename it to something you will remember easily. [DO NOT USE
    SPACES!]</li>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="0.2s">Go to: Steam\steamapps\common\dota 2 beta\game\dota\replays, find the .dem
    file that you renamed in step 1, copy it.</li>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="0.4s">Place the copy of the replay (demo) inside of the following path:
    Steam\steamapps\common\dota 2 beta\game\dota. Then close Dota 2.</li>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="0.6s">Launch Dota 2: Reborn Beta Tools, navigate to the Source Filmmaker.</li>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="0.8s">Create a new session / project, then right click the viewport and select &quot;Set Map&quot;,
    selecting the Dota 2 map. Let it load, then save.</li>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="1s">Select the Dota 2 task in your start bar and go to the options.</li>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="1.2s">Set video resolution to 1280x720, windowed // This helps make the replay easier
    to work with, and allows you to see more of the screen.</li>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="1.4s"> Open the console (Tilde key ~/`) inside of the game client.</li>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="1.6s">Open the console (Tilde key ~/`) inside of the game client.</li>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="1.8s">Once you have successfully loaded into the demo, in the console inside of the
    game client type: bind “Key” “demo_togglepause” (Replace the word Key with
    the button you&#39;d like to use to pause your demo when you are in between
    recordings). Close the console. NOTE: If your bind doesn&#39;t work, just type
    &quot;demo_pause&quot; or &quot;demo_togglepause&quot; (without the quotes of course) in the
    console inside of the client, when you need to pause in game.</li>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="2s">Find the spot in the demo that you want to record.</li>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="2.2s">Go back to the SFM, click record, then record the action. When you&#39;ve recorded
    the clip that you want, press the key we bound to pause the demo/replay in Step
    10.</li>
          <li class="wow fadeInLeft" data-wow-duration="1s" data-wow-delay="2.4s">Return to the SFM and press ESC. You now have a clip recorded from a replay
    that you can use all the power (nearly, seeing as how the SFM Reborn still needs
    some love) of the SFM on.</li>
        </ol>
