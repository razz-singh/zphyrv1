edit your replays. Here is a detail guide to help you make your own Dota 2 movie.</p>
        <p>First of all to use Dota 2 console commands you have to enable the console using
Steam’s launch options as this can’t actually be done from in-game.</p>
        <ul>
          <li>Open Steam. Right click on the game Dota 2 in your library.</li>
          <li>Select properties. Click on set launch options. Type –console and click on OK.</li>
          <li>When you open Dota 2, you should be seeing the console window in addition to the
        normal display.</li>
        </ul>
