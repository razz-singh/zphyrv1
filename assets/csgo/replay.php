          <h2>Functions of Built-in Demo Player:-</h2>
          <div class="fbi-demo col-md-8 col-md-offset-2 container">
            <ul>
              <li class="items">Anytime while playing back a demo file, the Demo Playback window can be brought up by
      pressing Shift+F2 or the demoui command. The Demo Playback window allows to pause and
      resume, change playback speed or move your camera viewpoint.</li>
              <li class="items">To load a new demo file hit the &quot;Load…&quot; button and choose a file from disc. The current
      loaded demo file name will be shown right beside the &quot;Load...&quot; button. The current and total
      playtime is shown right below. Using the slider on the right, you can adjust the playback
      speed between slow-motion and fast-forward (0% to 600%).</li>
              <li class="items">To change the camera&#39;s viewpoint, switch to &quot;Camera drive mode&quot; by pressing the &quot;Drive...&quot;
      toggle button. The button will stay in the pressed-down state as long as the &quot;Drive Mode&quot; is
      active. To navigate the camera, move the mouse out to the viewport, keep the LMB pressed
      and move around using the W, A, S, and D keys. You can move up and down with Z and X,
      and slow down with the Shift key.</li>
              <li class="items">There is also Demo Editor &amp; Demo Smoother option available in the Demo Playback
      window. The Demo Editor allows you to add and execute a set of commands during demo
      playback. These demo commands can display text, fade in/out, change the FOV or run any
      other console command. The Demo Smoother can be used to smooth out the recorded view
      or create completely new camera paths.</li>
            </ul>
