        <h2>Basic Recording &amp; Playback</h2>
        <div class="container col-md-8 col-md-offset-2">
            <ul class="ul_rec_steps">
              <li class="items">To record a game, type in console record &quot;name&quot;, while the file name shouldn&#39;t have any
    special characters or spaces. The demo file (.dem extension) is saved in your current game
    directory, e.g. \hl2\name.dem. You can start recording anytime within a running game and
    stop recording with the stop command.</li>
              <li class="items">If a changelevel occurs while recording, the current demo file will be closed and a new
    demo file name_2 (name_3, name_4, etc.) is created. To playback a previously recorded
    game, type playdemo &quot;name&quot;.</li>
              <li class="items">If you want to use demo playback for system benchmarking use timedemo &quot;name&quot;.
    Timedemo plays a demo as fast as possible and shows performance stats after the playback
    is finished (total drawn frames, seconds needed for playback, average frames per second
    and variability).</li>
            </ul>
        </div>
