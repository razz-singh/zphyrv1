             <h3>Career</h3>
          <p>Emil Pathric William Christensen (born 14 June 1984 in Stockholm), alias HeatoN, is a former
Swedish professional Counter-Strike player, formerly playing as captain of the gaming team Ninjas in
Pyjamas (NiP). He is widely considered as one of the best Counter-Strike players of all time. Emil&#39;s
first hobby was ice hockey, but he retired because of injury. A friend (CS player fRa1L, from
RevolutioN) told him to try Counter-Strike. Christensen was part of the original Ninjas in Pyjamas
team of 2001 which won the Cyberathlete Professional League World Championship that year. He
and the other members of Ninjas in Pyjamas went on to become the foundation to the highly
successful SK Sweden, one of the most successful teams in electronic sports history. Christensen led
SK Gaming to the World Cyber Games Counter-Strike championship in 2003 and to 4th place in 2004.
Christensen was nominated for Best Counter-Strike Player of the Year in 2004, losing out to SK
Gaming teammate, Tommy &quot;Potti&#39;&quot; Ingemarsson. He was selected for the Global Gaming League
European Counter-Strike All-Star team in 2005.At the beginning of 2005 he and the rest of SK
Sweden chose not to renew their contracts with SK Gaming and re-formed NiP. Since then some of
the NiP players moved back to SK Gaming but Christensen and others have stayed on at Ninjas in
Pyjamas. NiP are now competing at professional LAN tournaments again, after a hiatus in which
SpawN joined after HeatoN decided to take time off for personal reasons. Christensen, along with
the other members of Ninjas in Pyjamas, assisted in the design of the SteelSound 5H (USB)
Headphones produced by SteelSeries. In December 2006 he travelled to New York and Dallas to
provide support for his teammates as they competed in the WSVG world finals and CPL winter
respectively. NiP were expected to be strong contenders in both tournaments, however bowed out
uncharacteristically early. In September 2007 HeatoN quit playing Counter-Strike to be coach of NiP
but was then offered a chance to be a general manager for Stockholm in the CGS. HeatoN&#39;s CGS
franchise is called the Stockholm Magnetik and has a Counter-Strike: Source team, Dead or Alive 4
players, one male, one female, a FIFA 08 player and two Forza Motorsport 2 players to represent

him and his franchise. In September 2015 HeatoN was found guilty of fraud associated with the 2014
sale of NiP to Diglife, and was sentenced pay a fine and perform community service.</p>
